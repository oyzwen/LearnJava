package com.lesson4;

public class Suanshu {

    public static void main(String[] args) {
        // 算术
        int a = 1;
        int b = 2;
        int c = a + b;
        System.out.println("c:" + c);

        int d = c - a;
        System.out.println("d:" + d);

        int e = a * b;
        int f = b / a;
        System.out.println("e:" + e + "\nf:" + f);

        int g = c % b;
        System.out.println("g:" + g);
    }

}
