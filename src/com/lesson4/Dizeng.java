package com.lesson4;

public class Dizeng {

    public static void main(String[] args) {
        // ++递增   递增++ 递减--
        int a = 2;
        int b = 2;
        System.out.println(a++);
        System.out.println(++b);

        System.out.println(a);

        a--;
        b--;
        System.out.println("a:" + a + "\nb:" + b);
    }

}
